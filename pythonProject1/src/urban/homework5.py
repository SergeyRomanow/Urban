print('--->>> 2.СЛОВАРЬ:')
_dict = {
    'ГРУША': 'ЗЕЛЕНАЯ', 'ЯБЛОКО': 'КРАСНОЕ',
    'БАНАН': 'ЖЕЛТЫЙ', 'АПЕЛЬСИН': 'ОРАНЖЕВЫЙ',
    'КИВИ': 'ВОЛОСАТОЕ ЯБЛОКО'
}
print("--->>> СЛОВАРЬ (Dictionary) 3: ", "Тип переменной", type(_dict))
print("ЗНАЧЕНИЯ СЛОВАРЬ: ", _dict)
print("ЗНАЧЕНИЯ СЛОВАРЬ 'КИВИ': ", _dict['КИВИ'])

# ЗАМЕНА ЗНАЧЕНИЯ В СЛОВАРЕ
_dict['КИВИ'] = 'ИМПОРТНЫЙ'
print("ЗНАЧЕНИЯ СПИСКА 'КИВИ': ", _dict['КИВИ'])

# /brif ДОБАВЛЕНИЕ ЗНАЧЕНИЯ В СЛОВАРE
_dict['ПЕРСИК'] = 'ВОЛОСАТЫЙ'
print("ЗНАЧЕНИЯ СПИСКА 'ПЕРСИК': ", _dict['ПЕРСИК'])

# /brif ИТОГОВЫЙ ВАРИАНТ СЛОВАРЯ
print("ЗНАЧЕНИЯ СПИСКА: ", _dict)

print("ЗНАЧЕНИЯ СПИСКА 'ГРУША': ", _dict.get('ГРУША'))
print("ЗНАЧЕНИЯ СПИСКА 'БАНАН': ", _dict.get('БАНАН'))
