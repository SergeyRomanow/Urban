print('--->>> 2.СПИСОК:')
#           0           1        2       3       4         5
_list = ["АРЕЛЬСИН", "БАНАН", "ВИШНЯ", "КИВИ", "ГРУША", "ЯБЛОКО"]

print("--->>> СПИСОК (LIST): ", "Тип переменной", type(_list), 'ДЛИНА СПИСКА: ', len(_list))
print("--->>> ЗНАЧЕНИЯ СПИСКА: ", _list)
print("--->>> ЗНАЧЕНИЯ СПИСКА ПЕРВЫЙ: ", _list[0])
print("--->>> ЗНАЧЕНИЯ СПИСКА ПОСЛЕДНИЙ ПО ОТРИЦАТЕЛЬНОМУ ЧИСЛУ: ", _list[-1])
print("--->>> ЗНАЧЕНИЯ СПИСКА ПОСЛЕДНИЙ ПО ДЛИНЕ СПИСКА : ", _list[len(_list) - 1])
print('"--->>> ЗНАЧЕНИЯ СПИСКА C 3 - ДО 5 ', _list[2:4])

print("--->>> НЕИЗМЕНЕННЫЙ СПИСОК: ", _list)
_list[3] = 'ПЕРСИК'
print("--->>> ИЗМЕНЕННОЕ ЗНАЧЕНИЯ СПИСКА 3 ЭЛЕМЕНТ: ", _list[3])
print("--->>> НОВЫЕ ЗНАЧЕНИЯ СПИСКА: ", _list)


